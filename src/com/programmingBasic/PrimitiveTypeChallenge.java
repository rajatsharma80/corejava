package com.programmingBasic;

public class PrimitiveTypeChallenge {
    public static void main(String[] arg){
        byte myByte = 100;
        short myShort = 10000;
        int myInt = 1000-000-000;
        long myLong = 50-000L + ((myByte + myShort + myInt)*10);
        System.out.println("myLong: " + myLong);
    }
}
